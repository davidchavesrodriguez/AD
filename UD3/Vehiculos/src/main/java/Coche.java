import jakarta.persistence.Entity;

@Entity
public class Coche extends Vehiculo{
    private int puertas;
    private int plazas;

    public int getPuertas() {
        return puertas;
    }

    public void setPuertas(int puertas) {
        this.puertas = puertas;
    }

    public int getPlazas() {
        return plazas;
    }

    public void setPlazas(int plazas) {
        this.plazas = plazas;
    }
}
